package com.nextgptapp.here.data.repository

import android.util.Log
import com.google.firebase.firestore.FirebaseFirestore
import com.nextgptapp.here.data.model.browse.BrowseResult
import com.nextgptapp.here.data.model.browse.WebResults
import com.nextgptapp.here.data.model.browse.BraveSearchItem
import kotlinx.coroutines.tasks.await
import okhttp3.OkHttpClient
import okhttp3.Request
import org.json.JSONArray
import org.json.JSONObject
import java.net.URLEncoder
import javax.inject.Inject
import com.nextgptapp.here.data.model.browse.toWebContentList
import com.nextgptapp.here.data.model.browse.WebContent

class BraveSearchRepositoryImpl @Inject constructor(
    private val firestore: FirebaseFirestore,
    private val okHttpClient: OkHttpClient
) : BraveSearchRepository {

    override suspend fun searchBrave(query: String): BrowseResult? {
        return try {
            Log.d("BRAVE_DEBUG", "üî• === DIJAGNOSTIƒåKA BRAVE PRETRAGA ===")
            Log.d("BRAVE_DEBUG", "üìù Query: '$query'")

            // ‚úÖ Dohvaƒáanje konfiguracije
            val doc = firestore.collection("ai_models")
                .document("brave-search")
                .get()
                .await()

            if (!doc.exists()) {
                Log.e("BRAVE_DEBUG", "‚ùå Dokument 'brave-search' ne postoji!")
                return null
            }

            val apiKey = doc.getString("apiKey")
            val endpoint = doc.getString("apiEndpoint")

            Log.d("BRAVE_DEBUG", "üîë API Key: ${apiKey?.take(15)}...${apiKey?.takeLast(5)}")
            Log.d("BRAVE_DEBUG", "üåê Endpoint: $endpoint")

            if (apiKey.isNullOrBlank() || endpoint.isNullOrBlank()) {
                Log.e("BRAVE_DEBUG", "‚ùå API Key ili endpoint je prazan!")
                return null
            }

            // ‚úÖ Kreiraj URL
            val encodedQuery = URLEncoder.encode(query.trim(), "UTF-8")
            val url = "${endpoint.removeSuffix("/")}?q=$encodedQuery&count=5"

            Log.d("BRAVE_DEBUG", "üì° Konaƒçni URL: $url")

            // ‚úÖ Kreiraj request
            val request = Request.Builder()
                .url(url)
                .addHeader("Accept", "application/json")
                .addHeader("X-Subscription-Token", apiKey)
                .addHeader("User-Agent", "AskAI-Android/1.0")
                .build()

            Log.d("BRAVE_DEBUG", "üì¨ Request headers:")
            request.headers.forEach { (name, value) ->
                if (name == "X-Subscription-Token") {
                    Log.d("BRAVE_DEBUG", "üì¨   $name: ${value.take(10)}...${value.takeLast(5)}")
                } else {
                    Log.d("BRAVE_DEBUG", "üì¨   $name: $value")
                }
            }

            // ‚úÖ Pozovi API
            Log.d("BRAVE_DEBUG", "üöÄ ≈†aljem request...")
            val response = okHttpClient.newCall(request).execute()

            Log.d("BRAVE_DEBUG", "üì• Response status: ${response.code}")
            Log.d("BRAVE_DEBUG", "üì• Response message: '${response.message}'")
            Log.d("BRAVE_DEBUG", "üì• Response successful: ${response.isSuccessful}")

            // ‚úÖ Provjeri response headers
            Log.d("BRAVE_DEBUG", "üì• Response headers:")
            response.headers.forEach { (name, value) ->
                Log.d("BRAVE_DEBUG", "üì•   $name: $value")
            }

            if (!response.isSuccessful) {
                val errorBody = response.body?.string()
                Log.e("BRAVE_DEBUG", "‚ùå HTTP Error ${response.code}: '$errorBody'")
                return null
            }

            // ‚úÖ ƒåitaj response body
            val responseBody = response.body?.string()
            Log.d("BRAVE_DEBUG", "üì¶ Response body length: ${responseBody?.length ?: 0}")

            if (responseBody.isNullOrBlank()) {
                Log.e("BRAVE_DEBUG", "‚ùå Response body je prazan!")
                return null
            }

            // ‚úÖ KLJUƒåNO: Isprintaj CIJELI response za analizu
            Log.d("BRAVE_DEBUG", "üì¶ === CIJELI RESPONSE BODY ===")
            Log.d("BRAVE_DEBUG", responseBody)
            Log.d("BRAVE_DEBUG", "üì¶ === KRAJ RESPONSE BODY ===")

            // ‚úÖ Poku≈°aj parsirati JSON
            val json = try {
                JSONObject(responseBody)
            } catch (e: Exception) {
                Log.e("BRAVE_DEBUG", "‚ùå JSON parsing error: ${e.message}")
                Log.e("BRAVE_DEBUG", "‚ùå Response nije valjan JSON: '${responseBody.take(200)}'")
                return null
            }

            // ‚úÖ Analiziraj JSON strukturu
            Log.d("BRAVE_DEBUG", "üîç JSON root keys: ${json.keys().asSequence().toList()}")

            // ‚úÖ Poku≈°aj naƒái results u razliƒçitim lokacijama
            var resultsArray: JSONArray? = null
            var resultsLocation = "NEPOZNATO"

            when {
                json.has("web") -> {
                    Log.d("BRAVE_DEBUG", "üåê Prona≈°ao 'web' objekt")
                    val webObject = json.getJSONObject("web")
                    Log.d("BRAVE_DEBUG", "üåê 'web' keys: ${webObject.keys().asSequence().toList()}")

                    if (webObject.has("results")) {
                        resultsArray = webObject.getJSONArray("results")
                        resultsLocation = "web.results"
                        Log.d("BRAVE_DEBUG", "‚úÖ Prona≈°ao results u web.results")
                    }
                }
                json.has("results") -> {
                    Log.d("BRAVE_DEBUG", "üìÑ Prona≈°ao direktni 'results'")
                    resultsArray = json.getJSONArray("results")
                    resultsLocation = "results"
                }
                json.has("organic") -> {
                    Log.d("BRAVE_DEBUG", "üå± Prona≈°ao 'organic'")
                    resultsArray = json.getJSONArray("organic")
                    resultsLocation = "organic"
                }
                else -> {
                    Log.w("BRAVE_DEBUG", "‚ö†Ô∏è Nema poznatih result kljuƒçeva!")
                    Log.w("BRAVE_DEBUG", "‚ö†Ô∏è Dostupni kljuƒçevi: ${json.keys().asSequence().toList()}")

                    // ‚úÖ Isprintaj sadr≈æaj svih objekata da vidimo strukturu
                    json.keys().forEach { key ->
                        val value = json.get(key)
                        Log.d("BRAVE_DEBUG", "üîç Key '$key' type: ${value.javaClass.simpleName}")
                        if (value is JSONObject) {
                            Log.d("BRAVE_DEBUG", "üîç   Objekt '$key' keys: ${value.keys().asSequence().toList()}")
                        } else if (value is JSONArray) {
                            Log.d("BRAVE_DEBUG", "üîç   Array '$key' length: ${value.length()}")
                        } else {
                            Log.d("BRAVE_DEBUG", "üîç   Value '$key': $value")
                        }
                    }
                }
            }

            if (resultsArray == null) {
                Log.e("BRAVE_DEBUG", "‚ùå Nije pronaƒëen results array nigdje!")
                return null
            }

            Log.d("BRAVE_DEBUG", "üìä Results pronaƒëeni u: $resultsLocation")
            Log.d("BRAVE_DEBUG", "üìä Broj rezultata: ${resultsArray.length()}")

            // ‚úÖ Analiziraj svaki result item
            val items = mutableListOf<BraveSearchItem>()

            for (i in 0 until resultsArray.length()) {
                val item = resultsArray.getJSONObject(i)
                Log.d("BRAVE_DEBUG", "üìÑ === RESULT $i ===")
                Log.d("BRAVE_DEBUG", "üìÑ Keys: ${item.keys().asSequence().toList()}")

                // ‚úÖ Isprintaj sve kljuƒçeve i vrijednosti
                item.keys().forEach { key ->
                    val value = item.opt(key)
                    Log.d("BRAVE_DEBUG", "üìÑ   $key: ${value?.toString()?.take(100)}")
                }

                // ‚úÖ Poku≈°aj mapirati na BraveSearchItem
                val title = item.optString("title", "")
                val url = item.optString("url", "")
                val description = item.optString("description",
                    item.optString("snippet",
                        item.optString("meta_description", "")
                    )
                )
                val source = item.optString("source",
                    item.optString("hostname", "")
                )

                Log.d("BRAVE_DEBUG", "üìÑ Mapiran result $i:")
                Log.d("BRAVE_DEBUG", "üìÑ   Title: '$title'")
                Log.d("BRAVE_DEBUG", "üìÑ   URL: '$url'")
                Log.d("BRAVE_DEBUG", "üìÑ   Description: '${description.take(50)}...'")
                Log.d("BRAVE_DEBUG", "üìÑ   Source: '$source'")

                val braveItem = BraveSearchItem(
                    title = title,
                    url = url,
                    description = description,
                    source = source
                )

                items.add(braveItem)
            }

            val result = BrowseResult(
                query = query,
                web = WebResults(results = items)
            )

            Log.d("BRAVE_DEBUG", "‚úÖ === USPJE≈†NO ZAVR≈†ENO ===")
            Log.d("BRAVE_DEBUG", "‚úÖ Ukupno items: ${items.size}")
            Log.d("BRAVE_DEBUG", "‚úÖ Query: '${result.query}'")
            Log.d("BRAVE_DEBUG", "‚úÖ Web results size: ${result.web?.results?.size}")

            result

        } catch (e: Exception) {
            Log.e("BRAVE_DEBUG", "‚ùå Exception: ${e.message}", e)
            null
        }
    }
    override suspend fun getWebContent(query: String): List<WebContent> {
        val result = searchBrave(query)
        if (result == null) {
            Log.w("BRAVE_DEBUG", "‚ö†Ô∏è searchBrave vratio null, vraƒáam prazan WebContent list")
            return emptyList()
        }

        val list = result.toWebContentList()
        Log.d("BRAVE_DEBUG", "üìö WebContent konverzija zavr≈°ena, veliƒçina: ${list.size}")
        return list
    }

}